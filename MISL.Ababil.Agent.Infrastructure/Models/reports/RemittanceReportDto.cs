using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace MISL.Ababil.Agent.Infrastructure.Models.reports
{
    public class RemittanceReportDto
    {
        private String _agentName;
        private String _outletName;
        private String _outletAddress;
        private String _nameOfRemittanceInstitution;
        private String _remittanceName;
        private String _remittanceNo;
        private String _beneficiaryName;
        private String _beneficiaryAddress;
        private String _mobileNo;
        private String _userId;
        private Decimal _amount;
        private String _amountInWords;
        private long _trDate;

        public RemittanceReportDto(String agentName, String outletName,
            String outletAddress, String nameOfRemittanceInstitution,
            String remittanceName, String remittanceNo, String beneficiaryName,
            String beneficiaryAddress, String mobileNo, String userId,
            Decimal amount, long trDate)
        {
            this._agentName = agentName;
            this._outletName = outletName;
            this._outletAddress = outletAddress;
            this._nameOfRemittanceInstitution = nameOfRemittanceInstitution;
            this._remittanceName = remittanceName;
            this._remittanceNo = remittanceNo;
            this._beneficiaryName = beneficiaryName;
            this._beneficiaryAddress = beneficiaryAddress;
            this._mobileNo = mobileNo;
            this._userId = userId;
            this._amount = amount;
            this._trDate = trDate;
        }

        public RemittanceReportDto()
        {
           
        }

        public virtual string agentName
        {
            get
            {
                return _agentName;
            }
            set
            {
                this._agentName = value;
            }
        }

        public virtual string outletName
        {
            get
            {
                return _outletName;
            }
            set
            {
                this._outletName = value;
            }
        }

        public virtual string outletAddress
        {
            get
            {
                return _outletAddress;
            }
            set
            {
                this._outletAddress = value;
            }
        }

        public virtual string nameOfRemittanceInstitution
        {
            get
            {
                return _nameOfRemittanceInstitution;
            }
            set
            {
                this._nameOfRemittanceInstitution = value;
            }
        }

        public virtual string remittanceName
        {
            get
            {
                return _remittanceName;
            }
            set
            {
                this._remittanceName = value;
            }
        }


        public virtual string remittanceNo
        {
            get
            {
                return _remittanceNo;
            }
            set
            {
                this._remittanceNo = value;
            }
        }

        public virtual string beneficiaryName
        {
            get
            {
                return _beneficiaryName;
            }
            set
            {
                this._beneficiaryName = value;
            }
        }

        public virtual string beneficiaryAddress
        {
            get
            {
                return _beneficiaryAddress;
            }
            set
            {
                this._beneficiaryAddress = value;
            }
        }

        public virtual string mobileNo
        {
            get
            {
                return _mobileNo;
            }
            set
            {
                this._mobileNo = value;
            }
        }

        public virtual string userId
        {
            get
            {
                return _userId;
            }
            set
            {
                this._userId = value;
            }
        }

        public virtual Decimal amount
        {
            get
            {
                return _amount;
            }
            set
            {
                this._amount = value;
            }
        }

        public virtual String amountInWords
        {
            get
            {
                return _amountInWords;
            }
            set
            {
                this._amountInWords = value;
            }
        }
        public virtual long trDate
        {
            get
            {
                return _trDate;
            }
            set
            {
                this._trDate = value;
            }
        }
        
    }
}
